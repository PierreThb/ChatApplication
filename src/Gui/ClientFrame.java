    class waitForMessage extends Thread {

        public void run() {
            while (true) {
                if (client.getNewMessageBool()) {
                    displayMessage();
                    client.setNewMessageBool(false);
                }
            }
        }
    }

    class newClient extends Thread {

        public void run() {
            while (true) {
                if(client.getNewClientBool()){
                    setListClientConnected();
                    client.setNewClientBool(false);
                    index ++;
                }
            }
        }
    }

